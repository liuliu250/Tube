public class Human {

    public String name;
    public int age;

    public Place School;
    public boolean isMale;
    public boolean isMarried;

    public Human(String name, int age, Place School, boolean isMale, boolean isMarried) {
        this.name = name;
        this.age = age;
        this.School = School;
        this.isMale = isMale;
        this.isMarried = isMarried;
    }

}
